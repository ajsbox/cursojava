package cursojava;

public class OperadoresIgualdad {
	public static void main(String[] args) {
		int a = 50;
		int b = 7;
		
		if(a > b) {
			System.out.println("A es mayor");
		}
		if(a < b) {
			System.out.println("B es mayor");
		}
		
		if(a != 10) {
			System.out.println("La variable \"a\" es diferente de 10");
		}
	}

}
