package cursojava;

public class SentenciasBucle {
	public static void main(String[] args) {
		
		int a = 0;
		
		// while
		while (a < 5) {
			System.out.println(a);
			a++;
		}
		
		// do-while
		int i = 1;    
	    do{    
	        System.out.println(i);    
	    i++;    
	    } while(i<=10); 
	    
	    // for
	    for (int x = 0; x < 5; x++) {
	    	  System.out.println("Numero: " + x);;
	    	}

	}

}
