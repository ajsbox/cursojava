package cursojava;

public class OperadoresMatematicos {
	public static void main(String[] args) {
		
		int a = 40;
		int b = 20;
		int c;
		
		int suma = a + b;
		int resta = a - b;
		int multiplicacion = a * b;
		int division = a / b;
		int modulo = a % b;
		
		
		System.out.println("El resultado de " + a + " + " + b + " es " + suma);
		System.out.println("El resultado de " + a + " - " + b + " es " + resta);
		System.out.println("El resultado de " + a + " * " + b + " es " + multiplicacion);
		System.out.println("El resultado de " + a + " / " + b + " es " + division);
		System.out.println("El resultado de " + a + " % " + b + " es " + modulo);
		
	}

}
